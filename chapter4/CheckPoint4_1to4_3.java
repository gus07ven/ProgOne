package chapter4;

public class CheckPoint4_1to4_3 {

	public static void main(String[] args) {
		
		//What will the following program segments display?
		// a) 
		int x = 2;
		int y = x++;
		System.out.println(y);
		 
		// b)
		int a = 2;
		System.out.println(a++);
		
		// c)
		int b = 2;
		System.out.println(--b);
		
		// d)
		int c = 8;
		int d = c--;
		System.out.println(d);
		
	}
}
