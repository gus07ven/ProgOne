package chapter2;

public class BigLittle {

	public static void main(String[] args) {
		
		// The variables are: little and big. 
		// The literals are: 2, 2000, "The little number is ", "The big number is ".
		
		int little;
		int big;
		
		little = 2;
		big = 2000;
		System.out.println("The little number is " + little);
		System.out.println("The big number is " + big);
	}
}
